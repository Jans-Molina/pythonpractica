# Creacion de una lista
lista = ["apple", "banana", "cherry"]
print(lista)

print("")

# Puede tener elementos con el mismo valor
lista1 = ["apple", "banana", "cherry", "apple", "cherry"]
print(lista1)

print("")

# Saber cuantos elementos tiene la lista
lista2 = ["apple", "banana", "cherry"]
print(len(lista2))

print("")

# Los elementos de la lista pueden ser cualquier tipo de datos y contener diferentes datos.
list1 = ["apple", "banana", "cherry"]
list2 = [1, 5, 7, 9, 3]
list3 = [True, False, False]
list4 = ["abc", 34, True, 40, "male"]

print(list1)
print(list2)
print(list3)
print(list4)

print("")

# Puedes crear una lista con el constructor List()
lista3 = list(("apple", "banana", "cherry")) 
print(lista3)