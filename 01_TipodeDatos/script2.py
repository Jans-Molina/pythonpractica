#string
a = "Hello World"
print(type(a))
#int
b = 20
print(type(b))
#float
c = 20.5
print(type(c))
#complex
d = 1j
print(type(d))
#list
e = ["apple", "banana", "cherry"]
print(type(e))
#tuple
f = ("apple", "banana", "cherry")
print(type(f))
#range
g = range(6)
print(type(g))
#dict
h = {"name" : "John", "age" : 36}
print(type(h))
#set
i = {"apple", "banana", "cherry"}
print(type(i))
#frozenset
j = frozenset({"apple", "banana", "cherry"})
print(type(j))
#bool
k = True
print(type(k))
#bytes
l = b"Hello"
print(type(l))
#bytearray
m = bytearray(5)
print(type(m))
#memoryview
n = memoryview(bytes(5))
print(type(n))
#nonetype
o = None
print(type(o))